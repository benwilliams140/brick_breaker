class Brick:
	def __init__(self, x, y, teir):
		self.x = x
		self.y = y
		self.width = 90
		self.height = 40
		self.teir = teir
		
	def getX(self):
		return self.x
		
	def getY(self):
		return self.y
	
	def getWidth(self):
		return self.width
	
	def getHeight(self):
		return self.height
		
	def getTeir(self):
		return self.teir
